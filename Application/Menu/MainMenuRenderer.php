namespace Application\Menu;

/**
 * Clase para generar los menus del sistema
 *
 * @category   project
 * @package    Project_Menus
 * @copyright  ##$COPYRIGHT$##
 */
class MainMenuRenderer extends AbstractMenuRenderer
{

  /**
   * Render the menu
   */
  public function render($menu)
  {
    $this->document->appendChild( $this->htmlify($menu,$this->ulId,$this->ulClass) );
    return $this->document->saveHTML();
  }

}
